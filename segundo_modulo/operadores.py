
# Operadores aritméticos
x = 5
y = 10
print("suma", x + y)      # 15
print("resta", x - y)     # -5



# Operadores de multiplicación y división
print("multiplicación", x * y)  # 50
print("división", y / x)        # 2.0  (flotante)

# Operadores de módulo, potencia y división entera
y = 10
x = 5
print("potencia", y ** x)     # 100000 (10^5)
print("división entera", y // x)  # 2
print("división entera", 12 // 5) # 2 (sin decimales)


# Operadores de módulo
print("módulo", 10 % 5)  # 0 (división exacta)
print("módulo", 12 % 5)  # 2 (12 = 5*2 + 2)

# Ejemplos adicionales de operadores de módulo
par = 8
print(par % 2 == 0)  # True
print(par % 2)       # 0